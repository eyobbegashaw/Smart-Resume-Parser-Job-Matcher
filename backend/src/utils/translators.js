class Translator {
  constructor(language = 'en') {
    this.language = language;
    this.translations = translations;
  }

  /**
   * Translate a key
   */
  t(key, params = {}) {
    let text = this.translations[this.language]?.[key] || 
               this.translations.en[key] || 
               key;
    
    // Replace parameters
    Object.keys(params).forEach(param => {
      text = text.replace(`{${param}}`, params[param]);
    });
    
    return text;
  }

  /**
   * Set language
   */
  setLanguage(language) {
    if (this.translations[language]) {
      this.language = language;
    }
  }

  /**
   * Get available languages
   */
  getLanguages() {
    return Object.keys(this.translations);
  }

  /**
   * Translate object properties
   */
  translateObject(obj, keys = []) {
    const translated = { ...obj };
    
    keys.forEach(key => {
      if (obj[key]) {
        translated[key] = this.t(obj[key]);
      }
    });
    
    return translated;
  }

  /**
   * Translate array of objects
   */
  translateArray(array, key = 'name') {
    return array.map(item => ({
      ...item,
      [key]: this.t(item[key])
    }));
  }

  /**
   * Get translation with fallback
   */
  getTranslation(key, lang = null) {
    const language = lang || this.language;
    return this.translations[language]?.[key] || 
           this.translations.en[key] || 
           key;
  }

  /**
   * Check if key exists
   */
  hasTranslation(key, lang = null) {
    const language = lang || this.language;
    return !!(this.translations[language]?.[key] || this.translations.en[key]);
  }

  /**
   * Add custom translation
   */
  addTranslation(language, key, value) {
    if (!this.translations[language]) {
      this.translations[language] = {};
    }
    this.translations[language][key] = value;
  }

  /**
   * Detect language from text
   */
  detectLanguage(text) {
    // Check for Amharic characters (Ethiopic range: U+1200 to U+137F)
    const amharicRegex = /[\u1200-\u137F]/;
    return amharicRegex.test(text) ? 'am' : 'en';
  }

  /**
   * Translate text using AI (for dynamic content)
   */
  async translateText(text, targetLang) {
    if (!text || this.detectLanguage(text) === targetLang) {
      return text;
    }

    // In production, you'd call a translation API here
    // For now, return the original text
    return text;
  }
}

